tup1=(4,3,2,2,-1,18)
tup2=(2,4,8,8,3,2,9)
length1=len(tup1)
length2=len(tup2)
for t1 in range(0,length1):
    length1-=1
    ind1=0
    inda=1
    tup1[ind1*inda]
    ind1+=1
    inda+=1
for t2 in range(0,length2):
    length2-=1
    ind2=0
    indb=1
    tup2[ind2*indb]
    ind2+=1
    indb+=1
print(f'Product of first tuple:{t1}')
print(f'Product of second tuple:{t2}')